name = ("vijayKUMaR")

print(len(name))  # length of the variable
print(name.find("v"))  # shows where the character is using index value
print(name.capitalize())  # capitalize the first letter
print(name.upper())  # turns the char into upper case
print(name.lower())  # into lowe case
print(name.isdigit())  # checks if there is any integers, returns boolean value
print(name.isalpha())  # checks if DT contains alphabets, returns boolean value
print(name.count("a"))  # shows how many a's are in the variable
print(name.replace("vi", "a"))  # replace the character
print(name * 3)  # prints multiple times