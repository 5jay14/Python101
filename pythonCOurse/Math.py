import math


pi = 3.14
a = -3.14
x = 1
y = 2
z = 3


print(round(pi))  # prints the whole integer part of the number
print(math.ceil(pi))  # rounds the number up to the nearest whole integer
print(math.floor(pi))  # rounds the number down to the nearest whole integer
print(abs(a))  # shows how far the number is from 0
print(pow(pi, 2))  # power root
print(round(math.sqrt(36)))
print(max(x,y,z))  # finds the largest number
print(min(x,y,z))  # finds the minimum number

